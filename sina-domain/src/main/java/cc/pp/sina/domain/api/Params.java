package cc.pp.sina.domain.api;

public class Params {

	private String fl;
	private String q;
	private String start;
	private String rows;
	private String wt;
	private String _;

	public String getFl() {
		return fl;
	}

	public void setFl(String fl) {
		this.fl = fl;
	}

	public String getQ() {
		return q;
	}

	public void setQ(String q) {
		this.q = q;
	}

	public String getStart() {
		return start;
	}

	public void setStart(String start) {
		this.start = start;
	}

	public String getRows() {
		return rows;
	}

	public void setRows(String rows) {
		this.rows = rows;
	}

	public String getWt() {
		return wt;
	}

	public void setWt(String wt) {
		this.wt = wt;
	}

	public String get_() {
		return _;
	}

	public void set_(String _) {
		this._ = _;
	}

}
