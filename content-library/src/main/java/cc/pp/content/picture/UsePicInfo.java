package cc.pp.content.picture;

public class UsePicInfo {

	private long uid;
	private long wid;
	private String bind_username;
	private String url;
	private String md5;
	private String type;
	private String from;
	
	public long getUid() {
		return uid;
	}
	
	public void setUid(long uid) {
		this.uid = uid;
	}
	
	public long getWid() {
		return wid;
	}
	
	public void setWid(long wid) {
		this.wid = wid;
	}
	
	public String getBind_username() {
		return bind_username;
	}
	
	public void setBind_username(String bind_username) {
		this.bind_username = bind_username;
	}
	
	public String getUrl() {
		return url;
	}
	
	public void setUrl(String url) {
		this.url = url;
	}
	
	public String getMd5() {
		return md5;
	}
	
	public void setMd5(String md5) {
		this.md5 = md5;
	}
	
	public String getType() {
		return type;
	}
	
	public void setType(String type) {
		this.type = type;
	}
	
	public String getFrom() {
		return from;
	}
	
	public void setFrom(String from) {
		this.from = from;
	}

}
